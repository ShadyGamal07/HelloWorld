def drawCircle(r=.1,xc=0,yc=0):
    glBegin(GL_POLYGON)
    for theta in np.arange(0, 2 * pi, 0.01):
        x = r * cos(theta)
        y = r * sin(theta)
        glVertex(x+xc, y+yc)

    glEnd()
def drawLine(r=.1,xc=0,yc=0):
    glPointSize(4)
    glBegin(GL_POINTS)
    for theta in np.arange(0, 2 * pi, 0.01):
        x = r * cos(theta)
        y = r * sin(theta)
        glVertex(x+xc, y+yc)

    glEnd()



